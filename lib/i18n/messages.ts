const toLookup = (lang: AppLang) => {
  const lookup = new Map<string, string>();
  for (const phrase of PHRASES) {
    lookup.set(phrase.vi, phrase[lang]);
    lookup.set(phrase.en, phrase[lang]);
  }
  return lookup;
};

export const LOOKUP_EN = toLookup("en");
export const LOOKUP_VI = toLookup("vi");

export function resolvePhrase(input: string): PhraseValue | null {
  const text = input.trim();
  if (!text) return null;
  for (const phrase of PHRASES) {
    if (phrase.vi === text || phrase.en === text) return phrase;
  }
  return null;
}

export function translateText(input: string, lang: AppLang) {
  const text = input.trim();
  if (!text) return input;
  const translated = (lang === "en" ? LOOKUP_EN : LOOKUP_VI).get(text);
  if (!translated) return input;
  return input.replace(text, translated);
}
